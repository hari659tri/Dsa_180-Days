// C++ program to demonstrate ceil function
#include <cmath>
#include <iostream>
using namespace std;

// Driver function
int main()
{
	// using ceil function which return
	// floor of input value
	cout << " Ceil of 2.3 is : " << ceil(2.3) << endl;
	cout << " Ceil of -2.3 is : " << ceil(-2.3) << endl;

	return 0;
}
